# Aurum Circuit – Brand Protection Policy

The Aurum Circuit is a protected creative-scientific framework blending biomaterials, soft technology, human-first design, and narrative philosophy.  
This policy defines how the brand and its associated concepts are to be represented, referenced, and reproduced.

## Protected Terms
The following names and concepts are part of the Aurum Circuit Intellectual Framework:
- “Aurum Circuit”
- “Aurum Suit-System”
- “AU-Universe”
- “Bitdabloom”
- “Second-Skin Technology”
- “Soft-Strength Framework”
- “Human-First Mobility”
- “Seven Frequencies”

These may not be used, modified, or published without attribution.

## Brand Integrity
1. The Aurum Circuit may not be linked to harmful, exploitative, or deceptive products.  
2. No contributor may claim sole ownership of shared work.  
3. Representations must follow the project's ethics: no-harm, dignity-first, soft-technology, human-support.  
4. Logos, symbols, and narrative elements cannot be used for misleading or commercial purposes.

## Visual and Written Style
The brand must always be portrayed as:
- human-first  
- soft, supportive, protective  
- innovative but ethical  
- transparent  
- grounded in kindness and dignity

## Violations
Misuse of the Aurum Circuit identity, symbols, documents, or terms may result in:
- removal of access  
- legal notice  
- banning from contribution  
