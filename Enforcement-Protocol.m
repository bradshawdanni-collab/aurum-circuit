Aurum Circuit – Intellectual Property Enforcement Protocol

Owner: Danielle Austin
Project: Aurum Circuit – Human-First Soft Technology Framework
Effective Date: Upon publication in this repository

⸻

1. Purpose

This protocol outlines the steps taken when any Aurum Circuit intellectual property is used, copied, reproduced, or commercialised without authorisation.

It exists to protect:
	•	proprietary research
	•	creative terminology
	•	biomimetic pattern designs
	•	prototypes and concepts
	•	branding, naming, identity
	•	all written work and documentation

⸻

2. Covered Protections

This protocol is backed by:
	•	Aurum Circuit Proprietary License
	•	Legal Notice
	•	Brand Protection Policy
	•	IP Use Policy
	•	Trademark Notice
	•	Prior Art Declaration
	•	Master Governance Policy

⸻

3. What Constitutes a Violation

Any of the following will trigger enforcement action:
	•	copying files or ideas
	•	modifying and redistributing content
	•	commercial use of any material
	•	using the brand or terminology
	•	creating derivative products
	•	training AI models on this content
	•	claiming authorship or ownership
	•	misrepresenting affiliation with the creator

⸻

4. Enforcement Steps

Step 1 — Evidence Collection

When a violation is found:
	•	take screenshots
	•	save URLs
	•	timestamp everything
	•	record user/platform details
	•	archive the infringing content

Step 2 — Initial Notice

A friendly-but-firm message is sent:

“This content is protected under the Aurum Circuit Proprietary License.
Please remove it within 48 hours.”

Step 3 — Formal Takedown

If ignored:
	•	DMCA filing
	•	platform takedown request
	•	GitHub, Instagram, LinkedIn, Medium, etc.
	•	proof provided using this repository’s timestamps

Step 4 — Public Record

An entry is added to the private Infringement Log:
	•	date
	•	infringer
	•	link
	•	description
	•	action taken

Step 5 — Legal Escalation

Used only when necessary:
	•	cease-and-desist letter
	•	IP attorney consultation
	•	proof from GitHub commits & prior art

⸻

5. Penalties

Depending on severity:
	•	removal of content
	•	account/website takedowns
	•	public notice of misuse
	•	legal pursuit of damages
	•	long-term blacklisting from future collaborations

